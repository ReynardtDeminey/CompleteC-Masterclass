using System;

namespace Nums
{
    class Program
    {
        static void Main(string[] args)
        {
            // Integers
            int num1 = 13;
            int num2 = 15;
            int sumInts = num1 + num2;

            Console.WriteLine($"The sum of {num1} and {num2} is {sumInts}");

            //Double
            double doub1 = 10.5;
            double doub2 = 79.45;
            double sumDoubs = doub1 + doub2;

            Console.WriteLine($"The sum of {doub1} and {doub2} is {sumDoubs}");
        }
    }
}
